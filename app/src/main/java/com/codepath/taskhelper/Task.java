package com.codepath.taskhelper;

/**
 * Task is a container class to represent the tasks in the list.
 *
 * Each Task contains a text body, a priority from 0-2, and a due date in Unix time.
 *
 * @author Rowan-James Tran
 */
public class Task {
    public String body;
    public int priority;
    public long date;
}
